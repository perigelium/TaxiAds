public class CSSstyleAttrs
{
    public String color;
    public String fontWeight;
    public Float fontSize;
    public String textAlign;

    public CSSstyleAttrs(String color, String fontWeight, Float fontSize, String textAlign)
    {
        this.color = color;
        this.fontWeight = fontWeight;
        this.fontSize = fontSize;
        this.textAlign = textAlign;
    }
}
